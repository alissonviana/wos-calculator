// Current language (default: Portuguese)
let currentLanguage = localStorage.getItem('wos-language') || 'pt';

// Store original Portuguese labels
let originalLabels = {};

function t(key) {
    return translations[currentLanguage][key] || key;
}

// Store original Portuguese labels on first load
function storeOriginalLabels() {
    document.querySelectorAll('.resource-input label').forEach((label, idx) => {
        originalLabels[idx] = label.textContent.trim();
    });
}

function switchLanguage(lang) {
    if (lang !== 'pt' && lang !== 'en') return;

    currentLanguage = lang;
    localStorage.setItem('wos-language', lang);

    // Update all translatable elements
    updatePageLanguage();

    // Update dropdown value
    const selector = document.getElementById('language-selector');
    if (selector) {
        selector.value = lang;
    }
}

